-- 4. Unified Dashboard View
CREATE OR REPLACE VIEW CONSUMPTION_ASSET.GRANDATA.V_DISTRICT_SUMMARY AS
SELECT
    m.PROVINCE_CODE,
    m.PROVINCE_KOR_NAME,
    m.CITY_CODE,
    m.CITY_KOR_NAME,
    m.DISTRICT_CODE,
    m.DISTRICT_KOR_NAME,
    p.STANDARD_YEAR_MONTH,
    p.TOTAL_RESIDENTIAL,
    p.TOTAL_WORKING,
    p.TOTAL_VISITING,
    p.TOTAL_POPULATION,
    s.TOTAL_SALES,
    s.FOOD_SALES,
    s.COFFEE_SALES,
    s.ENTERTAINMENT_SALES,
    s.BEAUTY_SALES,
    s.MEDICAL_SALES,
    s.E_COMMERCE_SALES,
    i.AVG_INCOME,
    i.MEDIAN_INCOME,
    i.AVG_ASSET,
    i.AVG_CREDIT_SCORE
FROM CONSUMPTION_ASSET.GRANDATA.M_SCCO_MST m
LEFT JOIN CONSUMPTION_ASSET.GRANDATA.AGG_POPULATION_MONTHLY p
    ON m.DISTRICT_CODE = p.DISTRICT_CODE
LEFT JOIN CONSUMPTION_ASSET.GRANDATA.AGG_CARD_SALES_MONTHLY s
    ON m.DISTRICT_CODE = s.DISTRICT_CODE
    AND p.STANDARD_YEAR_MONTH = s.STANDARD_YEAR_MONTH
LEFT JOIN CONSUMPTION_ASSET.GRANDATA.AGG_INCOME_MONTHLY i
    ON m.DISTRICT_CODE = i.DISTRICT_CODE
    AND p.STANDARD_YEAR_MONTH = i.STANDARD_YEAR_MONTH;