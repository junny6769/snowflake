CREATE OR REPLACE TABLE CONSUMPTION_ASSET.GRANDATA.UNIFIED_DISTRICT_MONTHLY AS
WITH pop AS (
    SELECT
        DISTRICT_CODE,
        STANDARD_YEAR_MONTH,
        SUM(RESIDENTIAL_POPULATION) AS TOTAL_RESIDENTIAL,
        SUM(WORKING_POPULATION)     AS TOTAL_WORKING,
        SUM(VISITING_POPULATION)    AS TOTAL_VISITING,
        SUM(RESIDENTIAL_POPULATION + WORKING_POPULATION + VISITING_POPULATION) AS TOTAL_POPULATION
    FROM CONSUMPTION_ASSET.GRANDATA.FLOATING_POPULATION_INFO
    GROUP BY DISTRICT_CODE, STANDARD_YEAR_MONTH
),
sales AS (
    SELECT
        DISTRICT_CODE,
        STANDARD_YEAR_MONTH,
        SUM(TOTAL_SALES)                    AS TOTAL_SALES,
        SUM(FOOD_SALES)                     AS FOOD_SALES,
        SUM(COFFEE_SALES)                   AS COFFEE_SALES,
        SUM(ENTERTAINMENT_SALES)            AS ENTERTAINMENT_SALES,
        SUM(DEPARTMENT_STORE_SALES)         AS DEPARTMENT_STORE_SALES,
        SUM(LARGE_DISCOUNT_STORE_SALES)     AS LARGE_DISCOUNT_STORE_SALES,
        SUM(SMALL_RETAIL_STORE_SALES)       AS SMALL_RETAIL_STORE_SALES,
        SUM(CLOTHING_ACCESSORIES_SALES)     AS CLOTHING_ACCESSORIES_SALES,
        SUM(SPORTS_CULTURE_LEISURE_SALES)   AS SPORTS_CULTURE_LEISURE_SALES,
        SUM(ACCOMMODATION_SALES)            AS ACCOMMODATION_SALES,
        SUM(TRAVEL_SALES)                   AS TRAVEL_SALES,
        SUM(BEAUTY_SALES)                   AS BEAUTY_SALES,
        SUM(HOME_LIFE_SERVICE_SALES)        AS HOME_LIFE_SERVICE_SALES,
        SUM(EDUCATION_ACADEMY_SALES)        AS EDUCATION_ACADEMY_SALES,
        SUM(MEDICAL_SALES)                  AS MEDICAL_SALES,
        SUM(ELECTRONICS_FURNITURE_SALES)    AS ELECTRONICS_FURNITURE_SALES,
        SUM(CAR_SALES)                      AS CAR_SALES,
        SUM(CAR_SERVICE_SUPPLIES_SALES)     AS CAR_SERVICE_SUPPLIES_SALES,
        SUM(GAS_STATION_SALES)              AS GAS_STATION_SALES,
        SUM(E_COMMERCE_SALES)               AS E_COMMERCE_SALES,
        SUM(TOTAL_COUNT)                    AS TOTAL_COUNT,
        SUM(FOOD_COUNT)                     AS FOOD_COUNT,
        SUM(COFFEE_COUNT)                   AS COFFEE_COUNT,
        SUM(ENTERTAINMENT_COUNT)            AS ENTERTAINMENT_COUNT
    FROM CONSUMPTION_ASSET.GRANDATA.CARD_SALES_INFO
    GROUP BY DISTRICT_CODE, STANDARD_YEAR_MONTH
),
income AS (
    SELECT
        DISTRICT_CODE,
        STANDARD_YEAR_MONTH,
        AVG(AVERAGE_INCOME)                AS AVG_INCOME,
        AVG(MEDIAN_INCOME)                 AS MEDIAN_INCOME,
        AVG(AVERAGE_HOUSEHOLD_INCOME)      AS AVG_HOUSEHOLD_INCOME,
        AVG(AVERAGE_ASSET_AMOUNT)          AS AVG_ASSET,
        AVG(AVERAGE_SCORE)                 AS AVG_CREDIT_SCORE,
        SUM(CUSTOMER_COUNT)                AS TOTAL_CUSTOMER_COUNT,
        AVG(RATE_INCOME_UNDER_20M)         AS RATE_UNDER_20M,
        AVG(RATE_INCOME_20M_TO_30M)        AS RATE_20M_30M,
        AVG(RATE_INCOME_30M_TO_40M)        AS RATE_30M_40M,
        AVG(RATE_INCOME_40M_TO_50M)        AS RATE_40M_50M,
        AVG(RATE_INCOME_50M_TO_60M)        AS RATE_50M_60M,
        AVG(RATE_INCOME_60M_TO_70M)        AS RATE_60M_70M,
        AVG(RATE_INCOME_OVER_70M)          AS RATE_OVER_70M,
        AVG(RATE_MODEL_GROUP_LARGE_COMPANY_EMPLOYEE)    AS RATE_LARGE_COMPANY,
        AVG(RATE_MODEL_GROUP_GENERAL_EMPLOYEE)           AS RATE_GENERAL_EMPLOYEE,
        AVG(RATE_MODEL_GROUP_PROFESSIONAL_EMPLOYEE)      AS RATE_PROFESSIONAL,
        AVG(RATE_MODEL_GROUP_EXECUTIVES)                 AS RATE_EXECUTIVES,
        AVG(RATE_MODEL_GROUP_GENERAL_SELF_EMPLOYED)      AS RATE_GENERAL_SELF_EMPLOYED,
        AVG(RATE_MODEL_GROUP_PROFESSIONAL_SELF_EMPLOYED) AS RATE_PROFESSIONAL_SELF_EMPLOYED,
        AVG(RATE_MODEL_GROUP_OTHERS)                     AS RATE_OTHERS,
        AVG(AVERAGE_USAGE_AMOUNT)          AS AVG_CARD_USAGE,
        AVG(TOTAL_BALANCE_AMOUNT)          AS AVG_TOTAL_LOAN_BALANCE,
        AVG(AVERAGE_BALANCE_AMOUNT)        AS AVG_LOAN_PER_PERSON,
        AVG(RATE_HIGHEND)                  AS RATE_HIGHEND
    FROM CONSUMPTION_ASSET.GRANDATA.ASSET_INCOME_INFO
    GROUP BY DISTRICT_CODE, STANDARD_YEAR_MONTH
)
SELECT
    m.PROVINCE_CODE,
    m.PROVINCE_KOR_NAME,
    m.CITY_CODE,
    m.CITY_KOR_NAME,
    m.DISTRICT_CODE,
    m.DISTRICT_KOR_NAME,
    COALESCE(p.STANDARD_YEAR_MONTH, s.STANDARD_YEAR_MONTH, i.STANDARD_YEAR_MONTH) AS STANDARD_YEAR_MONTH,
    p.TOTAL_RESIDENTIAL,
    p.TOTAL_WORKING,
    p.TOTAL_VISITING,
    p.TOTAL_POPULATION,
    s.TOTAL_SALES,
    s.FOOD_SALES,
    s.COFFEE_SALES,
    s.ENTERTAINMENT_SALES,
    s.DEPARTMENT_STORE_SALES,
    s.LARGE_DISCOUNT_STORE_SALES,
    s.SMALL_RETAIL_STORE_SALES,
    s.CLOTHING_ACCESSORIES_SALES,
    s.SPORTS_CULTURE_LEISURE_SALES,
    s.ACCOMMODATION_SALES,
    s.TRAVEL_SALES,
    s.BEAUTY_SALES,
    s.HOME_LIFE_SERVICE_SALES,
    s.EDUCATION_ACADEMY_SALES,
    s.MEDICAL_SALES,
    s.TOTAL_COUNT,
    s.FOOD_COUNT,
    s.COFFEE_COUNT,
    s.ENTERTAINMENT_COUNT,
    i.AVG_INCOME,
    i.MEDIAN_INCOME,
    i.AVG_HOUSEHOLD_INCOME,
    i.AVG_ASSET,
    i.AVG_CREDIT_SCORE,
    i.TOTAL_CUSTOMER_COUNT,
    i.RATE_UNDER_20M,
    i.RATE_20M_30M,
    i.RATE_30M_40M,
    i.RATE_40M_50M,
    i.RATE_50M_60M,
    i.RATE_60M_70M,
    i.RATE_OVER_70M,
    i.RATE_LARGE_COMPANY,
    i.RATE_GENERAL_EMPLOYEE,
    i.RATE_PROFESSIONAL,
    i.RATE_EXECUTIVES,
    i.RATE_GENERAL_SELF_EMPLOYED,
    i.RATE_PROFESSIONAL_SELF_EMPLOYED,
    i.RATE_OTHERS,
    i.AVG_CARD_USAGE,
    i.AVG_TOTAL_LOAN_BALANCE,
    i.AVG_LOAN_PER_PERSON,
    i.RATE_HIGHEND
FROM CONSUMPTION_ASSET.GRANDATA.M_SCCO_MST m
LEFT JOIN pop p ON m.DISTRICT_CODE = p.DISTRICT_CODE
LEFT JOIN sales s ON m.DISTRICT_CODE = s.DISTRICT_CODE AND p.STANDARD_YEAR_MONTH = s.STANDARD_YEAR_MONTH
LEFT JOIN income i ON m.DISTRICT_CODE = i.DISTRICT_CODE AND p.STANDARD_YEAR_MONTH = i.STANDARD_YEAR_MONTH
