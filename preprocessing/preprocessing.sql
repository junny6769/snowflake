-- 1.Floating Population Aggregation

-- 1.1 city code & district code monthly summary
CREATE OR REPLACE TABLE CONSUMPTION_ASSET.GRANDATA.AGG_POPULATION_MONTHLY AS
SELECT
    PROVINCE_CODE,
    CITY_CODE,
    DISTRICT_CODE,
    STANDARD_YEAR_MONTH,
    SUM(RESIDENTIAL_POPULATION)                        AS TOTAL_RESIDENTIAL,
    SUM(WORKING_POPULATION)                            AS TOTAL_WORKING,
    SUM(VISITING_POPULATION)                           AS TOTAL_VISITING,
    SUM(RESIDENTIAL_POPULATION + WORKING_POPULATION + VISITING_POPULATION) AS TOTAL_POPULATION
FROM CONSUMPTION_ASSET.GRANDATA.FLOATING_POPULATION_INFO
GROUP BY PROVINCE_CODE, CITY_CODE, DISTRICT_CODE, STANDARD_YEAR_MONTH;

-- 1.2 Gender & age group summary
CREATE OR REPLACE TABLE CONSUMPTION_ASSET.GRANDATA.AGG_POPULATION_DEMO AS
SELECT
    DISTRICT_CODE,
    STANDARD_YEAR_MONTH,
    GENDER,
    AGE_GROUP,
    SUM(RESIDENTIAL_POPULATION) AS RESIDENTIAL,
    SUM(WORKING_POPULATION)    AS WORKING,
    SUM(VISITING_POPULATION)   AS VISITING
FROM CONSUMPTION_ASSET.GRANDATA.FLOATING_POPULATION_INFO
GROUP BY DISTRICT_CODE, STANDARD_YEAR_MONTH, GENDER, AGE_GROUP;

-- 1.3 Timeslot weekday_weekend summary
CREATE OR REPLACE TABLE CONSUMPTION_ASSET.GRANDATA.AGG_POPULATION_TIMESLOT AS
SELECT
    DISTRICT_CODE,
    STANDARD_YEAR_MONTH,
    TIME_SLOT,
    WEEKDAY_WEEKEND,
    SUM(RESIDENTIAL_POPULATION) AS RESIDENTIAL,
    SUM(WORKING_POPULATION)    AS WORKING,
    SUM(VISITING_POPULATION)   AS VISITING
FROM CONSUMPTION_ASSET.GRANDATA.FLOATING_POPULATION_INFO
GROUP BY DISTRICT_CODE, STANDARD_YEAR_MONTH, TIME_SLOT, WEEKDAY_WEEKEND;

